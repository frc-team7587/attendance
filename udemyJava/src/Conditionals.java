


public class Conditionals {
	public static void main(String[] args) {
	
		//if (some expression that is true)
		//			do something
		
		boolean sayHello = false;
		boolean sayHey = false;
		
		//only the first statement to evaluate to true gets executed
		if(sayHello) {
			System.out.println("Hello!");
		}
		else if(sayHey){
			System.out.println("Hey!");
		}
		else {
			System.out.println("Goodbye!");
		}
		
		
		// if(x = 5) assigns 5 to x
		// if (x == 5) tests whether x has the value 5, which is false
		
		// && AND, || OR
		
		
	}
}
